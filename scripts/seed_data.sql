        SELECT care_level, COUNT(*) as count
        FROM elderly
        GROUP BY care_level
        ORDER BY care_level
    LOOP
        RAISE NOTICE '  %: %', row.care_level, row.count;
    END LOOP;

    RAISE NOTICE '账单按状态分布：';
    FOR row IN
        SELECT status, COUNT(*) as count, ROUND(SUM(amount)::numeric, 2) as amount
        FROM bills
        GROUP BY status
        ORDER BY status
    LOOP
        RAISE NOTICE '  %: %笔, ¥%', row.status, row.count, row.amount;
    END LOOP;

END $$;
